package CoreJava.A01_JavaBasics;

public class PrintValue {
    static void main(String[] args) {
        int a = 10;
        float b = 100.0F;
        double c = 1200.0;
        char ch = 'a';
        long l = 1500;
        byte b1 = 10;
        short s1 = 100;

        System.out.println("integer value = " +a);
        System.out.println("Floating value = " +b);
        System.out.println("Double type value = " +c);
        System.out.println("Long type value = " +l);
        System.out.println("Character value = " +ch);
        System.out.println("Byte value = " +b1);
        System.out.println("short value = " +s1);
    }
}
